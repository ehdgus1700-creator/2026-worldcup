def get_win_probability(team_a, team_b):
    # 피파 랭킹과 몸값을 종합한 간단한 승리 확률 알고리즘
    data_a = df_market[df_market["국가"] == team_a].iloc[0]
    data_b = df_market[df_market["국가"] == team_b].iloc[0]
    
    # 파워 포인트 산출 (높을수록 좋음)
    # 피파랭킹 역수 반영 (1위가 가장 높도록) + 몸값 가중치
    power_a = (100 - data_a["피파랭킹"]) * 1.5 + data_a["시장가치_억유로"] * 2
    power_b = (100 - data_b["피파랭킹"]) * 1.5 + data_b["시장가치_억유로"] * 2
    
    total = power_a + power_b
    prob_a = (power_a / total) * 100
    prob_b = (power_b / total) * 100
    
    return prob_a, prob_b
